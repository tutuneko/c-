#include <iostream>
//本程序旨在练习 tutu neko（自此之后称之为T*N）的新风格。
int main()
{
    using namespace std;//引入命名空间

cout<<
"tutu neko\n";//输出tutu neko并换行
cout<<
"i don't care what you are doing"//输出i don't care what you are doing
<<endl;//换行
    
    return 0;//程序结束
}
/*
输出界面——————————————————
tutu neko
i don't care what you are doing
功用——————————————————————
only practice the new style of tutu neko.
总结——————————————————————
1.使用 \n 换行符来实现换行时，不能将其单独另起一段，必须紧跟在输出语句的末尾。
*/